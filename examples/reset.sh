#!/usr/bin/env bash
# Откат прогона нормализатора к состоянию из git: восстанавливает отслеживаемые
# файлы и удаляет неотслеживаемые объекты (например, опустевшие нормализованные
# каталоги). Выполняется без запроса.
#
# Безопасность (см. также раздел в README):
#  - работает ТОЛЬКО внутри каталога самого скрипта (.), никогда не трогает
#    файлы вне него.
#
# Вся логика обёрнута в функцию main и вызывается последней строкой: так
# интерпретатор читает скрипт целиком ДО выполнения и корректно отрабатывает,
# даже если git-операции перепишут сам файл на диске.
set -euo pipefail

main() {
    local here
    here="$(cd -- "$(dirname -- "$0")" && pwd)"
    cd "$here"

    if ! git rev-parse --is-inside-work-tree >/dev/null 2>&1; then
        echo "Это не git-репозиторий — откат через git недоступен." >&2
        exit 1
    fi

    # Откат с core.ignorecase=false и в порядке reset -> clean -> checkout. На
    # регистронезависимой ФС (Windows, macOS) переименования, отличающиеся ТОЛЬКО
    # регистром (CaseRule: archive -> Archive и т.п.), git обычными командами не
    # возвращает: путь считается тем же, имя не переименовывается. С ignorecase=false
    # мис-кейсовый объект становится неотслеживаемым, удаляется на шаге 2 и
    # пересоздаётся на шаге 3 уже с правильным регистром.

    # 1. Снимаем индекс к HEAD (если переименования были staged) — мис-кейс станет untracked.
    git -c core.ignorecase=false reset -q -- .

    # 2. Удаляем неотслеживаемые объекты (мис-кейс, опустевшие нормализованные каталоги и пр.).
    git -c core.ignorecase=false clean -fd -- .

    # 3. Возвращаем отслеживаемые файлы к версии из git (только в этом каталоге).
    git -c core.ignorecase=false checkout -- .

    echo "Готово: examples/ возвращён к состоянию из git." >&2
}

main "$@"
