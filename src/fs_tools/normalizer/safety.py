"""Барьеры безопасности имени: stem остаётся ОДНИМ компонентом пути на любой ОС.

Эти константы и санитайз нужны и при транслитерации (TransliterationRule), и как
финальный барьер при сборке имени (NameNormalizer) — поэтому вынесены сюда, чтобы
не дублироваться. Живут в режиме `normalizer` (а не в `shared`): проверка ничего не
переименовывает. Менять их ослабление нельзя (см. cross-platform-safety.mdc).
"""
from __future__ import annotations

import re

# Разделители пути ('/' на всех ОС, '\' на Windows) и ASCII-управляющие символы
# (0x00-0x1F, включая '\n'). Имя файла/папки не должно содержать ни одного из них:
# иначе нормализованное имя перестаёт быть одним компонентом пути.
_DIR_UNSAFE_RE = re.compile(r"[\\/\x00-\x1f]+")

# Символы, запрещённые в именах файлов Windows. Транслитерация легко их порождает
# из типографики ('«'->'<<', '»'->'>>', '“'/'”'->'"'). В отличие от разделителей
# пути их нельзя оставить даже в виде '-': пользователь хочет, чтобы такой «мусор»
# просто вырезался. Удаляем ВЕЗДЕ (не только по краям), иначе одиночный '<' в
# середине имени ломает os.rename на Windows (WinError 123).
_WIN_FORBIDDEN_RE = re.compile("[<>:\"|?*]+")


def enforce_safe_component(stem: str) -> str:
    """Гарантирует, что stem остаётся одним валидным компонентом пути.

    Разделители пути и управляющие символы заменяются на '-' (иначе os.rename
    истолковал бы '/'/'\\' как разделитель и молча увёл объект в другой каталог);
    запрещённые на Windows символы вырезаются. Порядок шагов канонический —
    одинаковый и при транслитерации, и в финальном барьере.
    """
    stem = _DIR_UNSAFE_RE.sub("-", stem)
    stem = _WIN_FORBIDDEN_RE.sub("", stem)
    return stem
