"""DateRule: распознавание дат и приведение к ISO YYYY-MM-DD."""
import pytest

from fs_tools.normalizer import DateRule


@pytest.mark.parametrize(
    "bare, expected",
    [
        ("20.05.2020", "2020-05-20"),
        ("2020.05.20", "2020-05-20"),
        ("2020/05/20", "2020-05-20"),
        ("1.2.2020", "2020-02-01"),
        # Соседние разделители вокруг даты -> '_':
        ("2020-05-05-file", "2020-05-05_file"),
        ("dump-2020-05-05", "dump_2020-05-05"),
        ("2020-05-05.file", "2020-05-05_file"),
        ("dump 20.05.2020", "dump_2020-05-20"),
        ("dump_2020-05-05", "dump_2020-05-05"),
        ("2020-05-00-file", "2020-05-00_file"),
        ("year-2020-end", "year_2020-00-00_end"),
        ("05.2020", "2020-05-00"),
        ("2020.05", "2020-05-00"),
        ("1.2020", "2020-01-00"),
        ("2020", "2020-00-00"),
        # Невалидные/нерелевантные — без изменений:
        ("31.02.2020", "31.02.2020"),
        ("13.2020", "13.2020"),
        ("1080", "1080"),
        ("12345", "12345"),
        # Цифры, склеенные с буквами, — НЕ дата (отдельный токен обязателен):
        ("model2020", "model2020"),
        ("version2021", "version2021"),
        ("abc1999x", "abc1999x"),
        ("build2024release", "build2024release"),
        ("2020s", "2020s"),
        # Разделители-токены (_) сохраняют распознавание года:
        ("file_2020", "file_2020-00-00"),
        # '_' — токен-разделитель нормализатора, не разделитель месяц-год:
        # двухполевая MM_YYYY датой НЕ считается (анти-коллизия с index_year),
        ("05_2020", "05_2020-00-00"),
        # но трёхполевая YYYY_MM_DD через '_' распознаётся (однозначна):
        ("2020_05_20", "2020-05-20"),
        # Регресс-страж идемпотентности: индекс + готовая ISO-дата не ломаются
        # (раньше '03_2021' читался как март-2021 и портил 2021-03-10):
        ("03_2021-03-10", "03_2021-03-10"),
        # Уже нормализованные — без изменений:
        ("2020-05-20", "2020-05-20"),
        ("2020-05-00", "2020-05-00"),
        ("2020-00-00", "2020-00-00"),
    ],
)
def test_date_rule(bare, expected):
    """Проверяет сценарий: date rule."""
    assert DateRule().apply(bare, is_dir=False) == expected
